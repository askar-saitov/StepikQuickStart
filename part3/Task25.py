n, k = map(int, input().split())
z = [0] + list(map(int, list(input())))
d = 1000000007

a = [1] + [0] * n  # от текущей до -k
q = [0] + [0] * n  # от текущей до 0
a[1] = 1 - z[1]
q[1] = a[1]

for i in range(2, n + 1):
    if z[i] == 1:
        a[i] = 0
        q[i] = q[i-1]
    else:
        ng = max(i-k-1, 0)
        a[i] = (q[i-1] - q[ng]) % d
        q[i] = q[i-1] + 1

print(a[n])
print(a)
print(q)


